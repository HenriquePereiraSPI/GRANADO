/* ============================================================
   <granado-icon>
   Renderiza um icone (SVG inline) a partir de um nome de preset.
   Opcionalmente envolve o icone em um fundo circular ou quadrado
   (estilo "val-icon" usado para indicadores de status).

   Atributos (todos opcionais):
     name        - nome do icone. Lista completa via GranadoIcon.list().
     size        - tamanho do icone em px. Default: 20.
     color       - cor do traco do icone. Default: "#1C5C31".
     background  - "true" envolve o icone em um fundo. Default: false.
     bgcolor     - cor do fundo. Default: versao clara da cor do icone.
     bgshape     - "circle" (default) ou "square" (cantos arredondados).
     bgsize      - tamanho do fundo em px. Default: size + 60%.

   API estatica:
     GranadoIcon.list()  -> array com todos os nomes de presets disponiveis.

   Exemplo:

   <script src="assets/WebComponents/granado-icon.js"></script>

   <granado-icon name="check"></granado-icon>
   <granado-icon name="alert-triangle" color="#9A5A00"></granado-icon>
   <granado-icon name="clock" color="#9A5A00" background="true"></granado-icon>
   <granado-icon name="user" color="#2C5A8C" background="true" bgshape="square"></granado-icon>
   <granado-icon name="package" size="32" color="#1C5C31" background="true"></granado-icon>
   ============================================================ */

class GranadoIcon extends HTMLElement {
  static get observedAttributes() {
    return ['name', 'size', 'color', 'background', 'bgcolor', 'bgshape', 'bgsize'];
  }

  static list() {
    return Object.keys(GranadoIcon._ICONS);
  }

  connectedCallback() { this._render(); }
  attributeChangedCallback() { if (this.isConnected) this._render(); }

  _render() {
    const name = this.getAttribute('name') || '';
    const size = parseInt(this.getAttribute('size') || '20', 10);
    const color = this.getAttribute('color') || '#1C5C31';
    const hasBg = this.getAttribute('background') === 'true';
    const bgColor = this.getAttribute('bgcolor') || this._lighten(color, 0.82);
    const bgShape = this.getAttribute('bgshape') === 'square' ? 'square' : 'circle';
    const bgSizeAttr = this.getAttribute('bgsize');
    const bgSize = bgSizeAttr ? parseInt(bgSizeAttr, 10) : (size + Math.round(size * 0.6));

    const iconSvg = this._iconHtml(name, size);
    this.style.display = this.style.display || 'inline-flex';

    if (hasBg) {
      const radius = bgShape === 'square' ? '6px' : '50%';
      this.innerHTML = `<span style="display:inline-flex;align-items:center;justify-content:center;width:${bgSize}px;height:${bgSize}px;border-radius:${radius};background:${bgColor};color:${color};flex-shrink:0;box-sizing:border-box">${iconSvg}</span>`;
    } else {
      this.innerHTML = `<span style="display:inline-flex;align-items:center;color:${color};line-height:0">${iconSvg}</span>`;
    }
  }

  _iconHtml(name, size) {
    const path = GranadoIcon._ICONS[name];
    if (!path) return '';
    return `<svg width="${size}" height="${size}" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round" style="display:block">${path}</svg>`;
  }

  _lighten(hex, amount) {
    const m = String(hex).match(/^#?([0-9a-f]{6})$/i);
    if (!m) return hex;
    const num = parseInt(m[1], 16);
    let r = (num >> 16) & 0xff, g = (num >> 8) & 0xff, b = num & 0xff;
    r = Math.round(r + (255 - r) * amount);
    g = Math.round(g + (255 - g) * amount);
    b = Math.round(b + (255 - b) * amount);
    return '#' + [r, g, b].map(x => x.toString(16).padStart(2, '0')).join('');
  }
}

